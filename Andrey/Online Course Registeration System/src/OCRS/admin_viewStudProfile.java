    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        String sName = studNameText.getText();
        String[] temp;
        String reader;
        int i=0, j=0;
        try {
            FileReader fr = new FileReader("Student_Profile_DB.txt");
            BufferedReader br = new BufferedReader (fr);
            reader = br.readLine();
            
            while(reader.length()!=0)
            {
                
                temp = reader.split(",");
                if(sName.equals(temp[0])){
                    table.setValueAt(temp[1], i, j);
                    table.setValueAt(temp[0], i, j+1);
                    table.setValueAt(temp[2], i, j+2);
                    table.setValueAt(temp[3], i, j+3);
                    i++;
                }
                reader = br.readLine();
                if ("testnull".equals(reader)){
                    System.out.println("This should not occur, only to handle NullPointerException.");
                }
                else if(reader == null){
                    br.close();
                    fr.close();                        
                    break;
                }
            }
            br.close();
            fr.close();             
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,"File Not Found" + e);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void viewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewButtonActionPerformed
        // TODO add your handling code here:
        int k = 0;
        String id, name;
        String reader;
        String[]temp;
        try{
            while(k<table.getRowCount()){
                String select = String.valueOf(table.getValueAt(k, 4));
                if(select.equals("true")){
                    id = table.getValueAt(k, 0).toString();
                    name = table.getValueAt(k, 1).toString();
                    try{
                        FileReader fr = new FileReader("Student_Profile_DB.txt");
                        BufferedReader br = new BufferedReader (fr);
                        reader = br.readLine();

                        while(reader.length()!=0){
                            temp = reader.split(",");
                            if(name.equals(temp[0])&&id.equals(temp[1])){
                                s_uName = temp[4];
                                br.close();
                                fr.close();
                                break;
                            }
                            reader=br.readLine();
                        }
                        br.close();
                        fr.close();
                    }
                    catch(Exception e){
                        JOptionPane.showMessageDialog(null, "File Not Found!" + e);
                    }
                    admin_manageStudProfile amsp = new admin_manageStudProfile(uname, s_uName);
                    amsp.setVisible(true);
                    this.dispose();
                    return;
                }
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "No Student is selected from the table, please select a student to proceed." + e);
        }
    }//GEN-LAST:event_viewButtonActionPerformed

