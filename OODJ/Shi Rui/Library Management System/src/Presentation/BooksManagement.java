    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        DefaultListModel Jlista = new DefaultListModel();
        jList1.setModel(Jlista);
        SearchBook sb= new SearchBook();
        
        sb.DSearch(jTextField1.getText(),"BookList.txt");
        Jlista.removeAllElements();
        //Jlista.addElement("BookID   BookTitle    Quantity    Location");
        for ( int i = 0; i < sb.BookList.size(); i++ )
        {
            Jlista.addElement( sb.BookList.toArray()[i]);
        }
    }//GEN-LAST:event_SearchActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        String select = jList1.getSelectedValue().toString();
        String AccArr[] = select.split(" ");
        int i=0;
        String BID="";
        for (String ss:AccArr)
        {if (i==0){BID= ss;}
         i++;}    
        DeleteBooks db = new DeleteBooks();
        db.Delete(BID,"BookList.txt");
        DefaultListModel DLM = new DefaultListModel();
        jList1.setModel(DLM);
    }//GEN-LAST:event_DeleteActionPerformed

