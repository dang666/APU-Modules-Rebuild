    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
        // TODO add your handling code here:
        if (ID_text.getText().equals("") || name_text.getText().equals("") || username_text.getText().equals("") || password_text.getText().equals("") || contactNumber_text.getText().equals("") || email_text.getText().equals("") || DegreeLevel_text.getSelectedItem().toString().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Missing Details\nPlease Fill in the blank", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            id = ID_text.getText();
            name = name_text.getText();
            username = username_text.getText();
            password= password_text.getText();
            contact= contactNumber_text.getText();
            email = email_text.getText();
            degree = DegreeLevel_text.getSelectedItem().toString();
            location = "SaveFile/Student.txt";
            userType = "Student";
            
            dispose();
            StudentClass sc = new StudentClass();
            sc.PassVariable(pv_id, pv_fn);
            sc.AddUser(id, name, username, password, contact, email, degree, location, userType);
        }
    }//GEN-LAST:event_ConfirmActionPerformed

