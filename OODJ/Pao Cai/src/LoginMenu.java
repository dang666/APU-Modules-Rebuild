    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        
        //open the file of admin.txt to validate whether is admin or not.
        if (username.getText().equals("") || password.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Missing Details\nPlease Fill in the blank", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            dispose();
            LoginClass lc = new LoginClass();
            String tp = username.getText();
            String pw = password.getText();
            lc.Login(tp,pw, userType);
        }
        
    }//GEN-LAST:event_LoginActionPerformed

