    private void reg_bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reg_bActionPerformed
        //Declaring objects and variables
        String line_sep = System.lineSeparator();
        Pattern digitCasePatten = Pattern.compile("[0-9 ]");
        String name,id,degree,level,uname,pwd,pwd_conf,sec_quest,sec_ans;
        String[] temp;

        //Assigning text to variables
        name=name_f.getText();
        id=id_f.getText().toLowerCase();
        degree=degree_s.getSelectedItem().toString();
        level=level_s.getSelectedItem().toString();
        uname=usr_f.getText();
        pwd=pwd_f.getText();
        pwd_conf=pwd_conf_f.getText();
        sec_quest=sec_quest_s.getSelectedItem().toString();
        sec_ans=sec_ans_f.getText();
        
        //Validating that all text fields are complete
        if (name.length() == 0 ){
            JOptionPane.showMessageDialog(null, "Please enter your name.");
            return;
        }
        else if (id.length() == 0){
            JOptionPane.showMessageDialog(null, "Please enter your ID.");
            return;
        }
        else if (degree.equals("Select Course")){
            JOptionPane.showMessageDialog(null, "Please select a course.");
            return;
        }
        else if (level.equals("Select Degree Level")){
            JOptionPane.showMessageDialog(null, "Please select what level you are in.");
            return;
        }
        else if (uname.length() == 0){
            JOptionPane.showMessageDialog(null, "Please enter a Username.");
            return;
        }
        else if (uname.length() < 8){
            JOptionPane.showMessageDialog(null, "Username has to be at least 8 characters long.");
            return;
        }
        else if (pwd.length() == 0){
            JOptionPane.showMessageDialog(null, "Please enter a Password.");
            pwd_f.setText("");
            pwd_conf_f.setText("");
            return;
        }
        else if (pwd.length() < 8){
            JOptionPane.showMessageDialog(null, "Password has to be at least 8 characters long.");
            pwd_f.setText("");
            pwd_conf_f.setText("");
            return;
        }
        else if (!digitCasePatten.matcher(pwd).find()) {
            JOptionPane.showMessageDialog(null, "Password has to have at least 1 numeric character.");
            pwd_f.setText("");
            pwd_conf_f.setText("");            
            return;
        }
        else if (sec_quest.equals("Select Question")){
            JOptionPane.showMessageDialog(null, "Please select a security question.");
            pwd_f.setText("");
            pwd_conf_f.setText("");            
            return;
        }
        else if (sec_ans.length() == 0){
            JOptionPane.showMessageDialog(null, "Please answer the security question.");
            pwd_f.setText("");
            pwd_conf_f.setText("");            
            return;
        }
        
        //Check if user exists, if exist then reset form
        try{
            FileWriter fw = new FileWriter ("Student_Profile_DB.txt", true);
            FileReader fr = new FileReader ("Student_Profile_DB.txt");
            BufferedReader br = new BufferedReader(fr);
            String reader;
            reader = br.readLine();
            
            if ("testnull".equals(reader)) {
                System.out.println("This shouldn't happen, please ignore this section, it's just to handle NullPointerException.");
                return;
            }
            else {

                //If no user exists then register all details
                if(reader == null){

                    //If password confirmation matches
                    if(pwd.equals(pwd_conf)){    
                        fw.write(name+","+id+","+degree+","+level+","+uname+","+pwd+","+sec_quest+","+sec_ans);
                        fw.write(line_sep);
                        fw.close();
                        JOptionPane.showMessageDialog(null, "User successfully registered, you can now log in.");
                        Login_Page lp = new Login_Page();
                        lp.setVisible(true);
                        br.close();
                        fr.close();                      
                        this.dispose();
                        return;
                    }

                    //If password confirmation does not match
                    else{
                        JOptionPane.showMessageDialog(null, "Password confirmation does not match, please try again.");
                        pwd_f.setText("");
                        pwd_conf_f.setText("");
                        br.close();
                        fr.close();
                        fw.close();                    
                        return;
                    }
                }            

                //If user exists, ask user to login or reset password instead, send back to login page
                while(reader.length() != 0){
                    temp = reader.split(",");
                    if(id.equals(temp[1])||uname.equals(temp[4])){
                        JOptionPane.showMessageDialog(null, "User already exists, please login or reset password.","Error",JOptionPane.ERROR_MESSAGE);
                        Login_Page lp = new Login_Page();
                        lp.setVisible(true);
                        br.close();
                        fr.close();
                        fw.close();                    
                        this.dispose();
                        return;
                    }
                    reader = br.readLine();
                    if ("testnull".equals(reader)) {
                        System.out.println("This shouldn't happen, please ignore this section, it's just to handle NullPointerException.");
                        return;
                    }
                    else {
                        //If user does not exist then register all details
                        if (reader == null){
                            //If password confirmation matches
                            if(pwd.equals(pwd_conf)){    
                                fw.write(name+","+id+","+degree+","+level+","+uname+","+pwd+","+sec_quest+","+sec_ans);
                                fw.write(line_sep);
                                fw.close();
                                JOptionPane.showMessageDialog(null, "User successfully registered, you can now log in.");
                                Login_Page lp = new Login_Page();
                                lp.setVisible(true);
                                br.close();
                                fr.close();                      
                                this.dispose();
                                return;
                            }

                            //If password confirmation does not match
                            else{
                                JOptionPane.showMessageDialog(null, "Password confirmation does not match, please try again.");
                                pwd_f.setText("");
                                pwd_conf_f.setText("");
                                br.close();
                                fr.close();
                                fw.close();                    
                                return;
                            }                            
                        }                      
                    }              
                }
            }
        }
        catch(Exception reg){
            JOptionPane.showMessageDialog(null, "System error" +reg);            
        }
    }//GEN-LAST:event_reg_bActionPerformed

