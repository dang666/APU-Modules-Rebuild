    private void man_bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_man_bActionPerformed
        String uname, name, id, degree, lvl, course = "", reader;
        String[] temp;
        String line_sep = System.lineSeparator();
        
        uname = usr_lbl.getText();
        name = name_f.getText();
        id = id_f.getText();
        degree = degree_s.getSelectedItem().toString();
        lvl = level_s.getSelectedItem().toString();
        course = course_f.getText().replaceAll(" ", "").toUpperCase();
        
        try{
            FileReader fr = new FileReader("Student_Profile_DB.txt");
            BufferedReader br = new BufferedReader(fr);
            reader = br.readLine();
            while (reader.length() != 0){
                temp = reader.split(",");
                if (uname.equals(temp[4])){
                    if(name.equals("Enter new name")&&id.equals("Enter new ID")&&degree.equals("Select Course")&&lvl.equals("Select Degree Level")&&course.equals("")){
                        br.close();
                        fr.close();
                        JOptionPane.showMessageDialog(null, "No changes made.");
                        return;
                    }
                    else{
                        int return_opt = 0;
                        return_opt = JOptionPane.showConfirmDialog(null, "Confirm change profile details?", "WARNING", JOptionPane.OK_CANCEL_OPTION);                        
                        if(return_opt == JOptionPane.OK_OPTION){
                            br.close();
                            fr.close();
                            Modify_Files mf = new Modify_Files();
                            String status = mf.replaceStudDetails(uname, name, id, degree, lvl, course);
                            if(status.equals("all deleted")){
                                JOptionPane.showMessageDialog(null, "Changes saved!");
                                Stud_vProfile svp = new Stud_vProfile(uname);
                                svp.setVisible(true);
                                this.dispose();
                                return;                                
                            }
                            else{
                                JOptionPane.showMessageDialog(null, "Changes saved!"+line_sep+"Unable to remove certain courses, please try again.");
                                Stud_manageprofile smp = new Stud_manageprofile(name,id,degree,lvl,uname);
                                smp.setVisible(true);
                                this.dispose();
                                return;  
                            }
                        }
                        else{
                            br.close();
                            fr.close();
                            return;                           
                        }
                    }
                }
                reader = br.readLine();
                if ("testnull".equals(reader)){
                    System.out.println("This should not occur, only to handle NullPointerException.");
                }
                else if (reader == null){
                    br.close();
                    fr.close();                        
                    break;                                
                }                
            }            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "System error, unable to make changes " + e);
        }
    }//GEN-LAST:event_man_bActionPerformed

