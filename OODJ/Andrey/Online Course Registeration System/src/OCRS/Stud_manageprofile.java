    public Stud_manageprofile(String name, String id, String deg, String lvl, String uname) {
        initComponents();    
        name_lbl.setText(name);
        id_lbl.setText(id);
        degree_lbl.setText(deg);
        lvl_lbl.setText(lvl);
        usr_lbl.setText(uname);
        try{
            FileReader fr = new FileReader ("Enrollment_List.txt");
            BufferedReader br = new BufferedReader(fr);
            String reader;
            String[] temp;
            reader = br.readLine();
            int i = 0, j = 0;
            if ("testnull".equals(reader)){
                System.out.println("This should not occur, only to handle NullPointerException.");
            }
            else{
                if (reader == null){
                    br.close();
                    fr.close();
                    return;
                }
                else{
                    while (reader.length() != 0) {
                        temp = reader.split(",");
                        if (uname.equals(temp[0])) {
                            course_t1.setValueAt(temp[1], i, j);
                            course_t1.setValueAt(temp[2], i, j+1);
                            i++;
                        }
                        reader = br.readLine();
                        if ("testnull".equals(reader)){
                            System.out.println("This should not occur, only to handle NullPointerException.");
                        }
                        else if(reader == null){
                            br.close();
                            fr.close();
                            return;
                        }                
                    }
                    br.close();
                    fr.close();                    
                }
            }
        } catch (Exception f) {
            JOptionPane.showMessageDialog(null, "System error " + f);
        }        
    }    

