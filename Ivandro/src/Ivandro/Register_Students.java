    private void jButtonAddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddStudentActionPerformed
        String id= txt_studentid.getText();
        String firstname = txt_firstname.getText();
        String lastname = txt_surname.getText();
        String birth= txt_dob.getText();
        String email= txt_email.getText();
        String phone= txt_tel.getText();
        
        String address = txt_address.getText();
        String house= txt_house.getText();
        String course =(String) jComboboxcourse.getSelectedItem();
     
        if (id.isEmpty() || firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || birth.isEmpty() 
                || phone.isEmpty() || email.isEmpty() || address.isEmpty() || house.isEmpty() || course.isEmpty()) {
            JOptionPane.showMessageDialog(this, " Please Fill all fields");}
        else {
            
            
            Student student = new Student(id, firstname, lastname, birth , gender, email, phone , address, house, course );
        
            try{
                // Creates or appends a File for Students
                PrintWriter pw = new PrintWriter(new FileWriter("Students.txt",true)); // method to print objects to a field
                pw.println(student.toString());
                JOptionPane.showMessageDialog(null,"Data is saved successfully");
                pw.flush();
                pw.close(); // Closes the file
                
            } catch (IOException ex) {
               JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButtonAddStudentActionPerformed

