    private void jButtonLecturerLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLecturerLoginActionPerformed
        // TODO add your handling code here:
        // Password and Username set to the text fields
        String password = jTextFieldLecturerPassword.getText();
        String username = jTextFieldLecturerUsername.getText();

        int x = 0;
        
       FileReader fr = null;
        
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Fill both fields"); } 
        else{
        try {
            File Students = new File ("Lecturer.txt");
            fr = new FileReader(Students);
            BufferedReader br = new BufferedReader(fr);
            String data;
            Lecturer lecturer;
            
            while ((data = br.readLine()) != null){
             lecturer = new Lecturer(data);
           
                
                if (lecturer.getId().contains(username))
                {
                    
                    if (lecturer.getLastname().contains(password)){
                        x =2;
                    JOptionPane.showMessageDialog(null,"Login Successful");
                    close();
                    Lecturer_Menu openwindow = new Lecturer_Menu();
                    openwindow.setVisible(true);
                    }
                  
                    
                }
            
        
            }
            
            
            
            
        } catch (FileNotFoundException ex) {
            
        
        }   catch (IOException ex) {
                Logger.getLogger(Lecturer_Login.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
            try {
                fr.close();
                
                
            } catch (IOException ex) {
                Logger.getLogger(Admin_SearchRecords.class.getName()).log(Level.SEVERE, null, ex);
            }
        }       
        if (x!=2){
                      JOptionPane.showMessageDialog(null,"Incorrect Password or Username \n Press OK to try again","Wrong Password", JOptionPane.ERROR_MESSAGE);
                  }
        }
    }//GEN-LAST:event_jButtonLecturerLoginActionPerformed

